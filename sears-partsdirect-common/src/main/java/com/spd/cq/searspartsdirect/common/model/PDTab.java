package com.spd.cq.searspartsdirect.common.model;

public class PDTab {
	private String tabsLink;
	private String tabsName;
	
	public PDTab() {
		// nullary constructor for Gson
	}

	public String getTabsLink() {
		return tabsLink;
	}

	public void setTabsLink(String tabsLink) {
		this.tabsLink = tabsLink;
	}

	public String getTabsName() {
		return tabsName;
	}

	public void setTabsName(String tabsName) {
		this.tabsName = tabsName;
	}
	
}
