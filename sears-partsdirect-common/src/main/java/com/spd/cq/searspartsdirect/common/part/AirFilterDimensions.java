package com.spd.cq.searspartsdirect.common.part;

import java.util.List;

public class AirFilterDimensions {
	
	private List<String> length;
	private List<String> width;
	private List<String> height;

	public List<String> getLength() {
		return length;
	}

	public void setLength(List<String> length) {
		this.length = length;
	}

	public List<String> getWidth() {
		return width;
	}

	public void setWidth(List<String> width) {
		this.width = width;
	}

	public List<String> getHeight() {
		return height;
	}

	public void setHeight(List<String> height) {
		this.height = height;
	}
}
